uint8_t local_addr=1;
uint16_t usMBCRC16(uint8_t *ptr, uint32_t len)
{
    uint8_t crchi = 0xff;
	uint8_t crclo = 0xff; 
	uint16_t index;
	uint8_t ch;
	int i=0;
	while (len--) 
	{
		ch = *ptr++;
		index = crclo ^ ch; 
		crclo = crchi ^ crctablehi[index];
		crchi = crctablelo[index];
		//printf("%d:%02x\t%04x\n",i,ch,crchi << 8 | crclo);
		i++;
	}
	return (crchi << 8 | crclo);

}
/*
发送 错误反馈
*/
void mb_sentACK(uint8_t cm, uint8_t err,_serialbuf_st *tx)
{
    uint16_t temp;
    tx->buf[0] = local_addr;
    tx->buf[1] = cm + 0x80;
    tx->buf[2] = err;
    temp = usMBCRC16(tx->buf, 3);
    tx->buf[3] = temp; //低
    tx->buf[4] = temp >> 8;
    tx->len=5;
}
/*
回应 读保持寄存器，命令0X03
*/
void mb_sentfor_readHoldingReg(const _mbdata_st mbd,_serialbuf_st *tx)
{
    uint16_t temp;
    uint8_t len = mbd.len * 2 + 5;
    tx->buf[0] = local_addr;
    tx->buf[1] = 0x03;
    tx->buf[2] = mbd.len * 2;
    /*用户数据*/
    for (temp = 0; temp < (len - 5) / 2; temp++)
    {
        tx->buf[3 + temp * 2] = mbd.buf[mbd.start + temp] >> 8;
        tx->buf[4 + temp * 2] = mbd.buf[mbd.start + temp];
    }
    temp = usMBCRC16(tx->buf, len - 2);
    tx->buf[len - 2] = temp; //低
    tx->buf[len - 1] = temp >> 8;
    tx->len=len;
    //ESP_LOGI("serial  ", "tx->len %d", tx->len);
    esp_log_buffer_hex("tx  ", tx->buf, tx->len);
}
/*
回应 写保持寄存器，命令0X10
*/
void mb_sentfor_writeHoldingReg(_serialbuf_st rx,_mbdata_st *pmb,_serialbuf_st *tx)
{
    uint16_t temp;
    uint8_t i;
    temp = usMBCRC16(rx.buf, 6);
    
    memcpy(tx->buf,rx.buf,6);
    tx->buf[6] = temp;
    tx->buf[7] = temp >> 8;
    tx->len=8;

    //修改保持寄存器
    for (i = 0; i < pmb->len; i++)
    {
        pmb->buf[pmb->start + i] = (uint16_t)(rx.buf[i * 2 + 7] << 8) + rx.buf[i * 2 + 8];
    }
    esp_log_buffer_hex("pmb->buf  ", pmb->buf, 10);

}
/*帧检测*/
uint8_t frm_cheak(_serialbuf_st *rx, _mbfrm_st *pfrm)
{
    uint16_t t, len;
    if (rx->len < 4)
        return res_ERR1;
    len = rx->len;
    rx->len = 0;

    pfrm->addr = rx->buf[0];
    if (pfrm->addr != local_addr)
        return res_ERR2;
    pfrm->crc = (uint16_t)(rx->buf[len - 1] << 8) + (rx->buf[len - 2]);
    t = usMBCRC16(rx->buf, len - 2);
    //ESP_LOGI("serial  ", "crc %d", t);
    if (pfrm->crc != t)
        return res_ERR3;
    pfrm->cmd = rx->buf[1];
    pfrm->start = (uint16_t)(rx->buf[2] << 8) + rx->buf[3];
    pfrm->rlen = (uint16_t)(rx->buf[4] << 8) + rx->buf[5];
    if (pfrm->start > 0x7d)
        return res_ERR4; //超出地址
    if (pfrm->rlen + pfrm->start > 0x7d)
        return res_ERR5; //超出长度
    return res_OK;
}

//接收
uint8_t smb_recvHoldingReg(_mbdata_st *pmb,_serialbuf_st serialRXbuf_st,_serialbuf_st *tx)
{
    uint8_t rel;
    _mbfrm_st frm;

    rel = frm_cheak(&serialRXbuf_st, &frm);
    ESP_LOGI("serial  ", "rel %d", rel);
    // ESP_LOGI("serial  ", "frm.addr %d", frm.addr);
    // ESP_LOGI("serial  ", "frm.crc %d", frm.crc);
    // ESP_LOGI("serial  ", "frm.rlen %d", frm.rlen);
    if (rel == res_OK)
    {
        gpio_set_level(MODRXorTX_GPIO, 0);
        //延时，给主机准备时间
        vTaskDelay(10 / portTICK_PERIOD_MS);
        pmb->len = frm.rlen;
        pmb->start = frm.start;
        switch (frm.cmd)
        {
        case 0x03: //读寄存器
            mb_sentfor_readHoldingReg(*pmb,tx);
            break;
        case 0x10: //写寄存器
            mb_sentfor_writeHoldingReg(serialRXbuf_st,pmb,tx);
            break;
        }
        gpio_set_level(MODRXorTX_GPIO, 1);
    }
    else if (rel == res_ERR3)
    {
        mb_sentACK(frm.cmd, rel,tx);
    }
    return rel;
}
